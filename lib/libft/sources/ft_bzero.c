#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*chr_s;
	size_t	i;

	chr_s = (char *)s;
	i = 0;
	while (i < n)
	{
		*(chr_s++) = 0;
		i++;
	}
}
